pub fn add_i(num_one: i32, num_two: i32) -> i32 {
    num_one + num_two
}

pub fn subtract_i(num_one: i32, num_two: i32) -> i32 {
    num_one - num_two
}

pub fn multiply_i(num_one: i32, num_two: i32) -> i32 {
    num_one * num_two
}

pub fn divide_i(num_one: i32, num_two: i32) -> i32 {
    num_one / num_two
}